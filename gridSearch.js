function gridSearch(G, P) {
	// Write your code here
	const l = P[0].length,
		iRange = G.length - P.length,
		jRange = G[0].length - l;
	let g, p;
	for (let i = 0; i <= iRange; i++)
		for (let j = 0; j <= jRange; j++) {
			if (G[i].slice(j, j + l) !== P[0]) continue;
			g = i + 1;
			p = 1;
			while (p < P.length) {
				if (G[g].slice(j, j + l) !== P[p]) break;
				p++;
				g++;
			}
			if (p === P.length) return "YES";
		}
	return "NO";
}

