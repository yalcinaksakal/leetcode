/**
 * @param {string} s
 * @return {number}
 */
var longestValidParentheses = function (s) {
  const isValid = s => {
    if (s.length % 2) return false;

    if (s.replace(/\(/g, "").length !== s.length / 2) return false;

    let numOpen = 0;

    for (let i = 0; i < s.length; i++) {
      if (numOpen < 0) return false;
      numOpen += s[i] === "(" ? 1 : -1;
    }

    return numOpen === 0;
  };

  let i = 0,
    length = s.length,
    maxValid = 0,
    j;

  while (i < length - maxValid - 1) {
    j = length;
    while (j > i + maxValid) {
      if (isValid(s.slice(i, j))) maxValid = j - i;
      j--;
    }
    i++;
  }

  return maxValid;
};

